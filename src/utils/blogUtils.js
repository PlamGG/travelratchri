export const addBlogPost = (title, content, category, image) => {
  const newPost = {
    id: Date.now(),
    title,
    content,
    date: new Date().toLocaleDateString(),
    category,
    image,
  };
  blogPosts = [newPost, ...blogPosts];
  return newPost;
};

export const getBlogPosts = () => {
  return blogPosts;
};

export const getBlogPostById = (id) => {
  return blogPosts.find(post => post.id === id);
};

export const getRelatedPosts = (id, limit = 3) => {
  const currentPost = getBlogPostById(id);
  if (!currentPost) return [];
  
  return blogPosts
    .filter(post => post.id !== id && post.category === currentPost.category)
    .slice(0, limit);
};

export const getBlogPostsByCategory = (category) => {
  return blogPosts.filter(post => post.category.toLowerCase() === category.toLowerCase());
};
